SELECT
	OWNER,
	SEGMENT_NAME,
	SEGMENT_TYPE,
	PARTITION_NAME,
	EXTENTS,
	BYTES/1024/1024 MB
FROM
	DBA_SEGMENTS
WHERE
	TABLESPACE_NAME = UPPER('&TABLESPACE_NAME')
ORDER BY
	BYTES DESC
